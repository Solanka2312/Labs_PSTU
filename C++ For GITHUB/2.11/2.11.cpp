#include <iostream>
using namespace std;

int main() {
    int n; // количество элементов в последовательности
    
    // Вводим сколько чисел будет
    cout << "Введите количество элементов: ";
    cin >> n;
    
    // Проверяем, что есть что вводить
    if (n <= 0) {
        cout << "Нет элементов для проверки!" << endl;
        return 1;
    }
    
    int number;          // для хранения текущего числа
    int whatFirst = 0;   // 0 - еще не определили, 1 - положительные, -1 - отрицательные
    bool found = false;  // нашли ли первое ненулевое число
    
    // Вводим все числа
    for (int i = 1; i <= n; i++) {
        cout << "Введите число " << i << ": ";
        cin >> number;
        
        // Если еще не определили, что первое, и текущее число не ноль
        if (!found) {
            if (number > 0) {
                whatFirst = 1;      // запоминаем, что положительные первыми
                found = true;       // больше не проверяем
            } else if (number < 0) {
                whatFirst = -1;     // запоминаем, что отрицательные первыми
                found = true;       // больше не проверяем
            }
            // если number == 0, ничего не меняем, продолжаем искать
        }
    }
    
    // Выводим результат
    cout << "Результат: ";
    if (whatFirst == 1) {
        cout << "Первыми введены положительные числа" << endl;
    } else if (whatFirst == -1) {
        cout << "Первыми введены отрицательные числа" << endl;
    } else {
        cout << "Все числа были нулевыми" << endl;
    }
    
    return 0;
}