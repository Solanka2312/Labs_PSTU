#include <iostream>
using namespace std;

int main() {
    int n; // количество элементов в последовательности
    
    // Вводим сколько чисел будет в последовательности
    cout << "Введите количество элементов: ";
    cin >> n;
    
    // Проверяем, что последовательность не пустая
    if (n <= 0) {
        cout << "Последовательность пустая!" << endl;
        return 1;
    }
    
    int firstMax;   // первый максимальный элемент
    int position = 1; // позиция первого максимума (начинаем с 1)
    int number;     // текущее число
    
    // Вводим первое число
    cout << "Введите число 1: ";
    cin >> firstMax;
    
    // Остальные числа вводим в цикле
    for (int i = 2; i <= n; i++) {
        cout << "Введите число " << i << ": ";
        cin >> number;
        
        // Если нашли число больше текущего максимума
        if (number > firstMax) {
            firstMax = number;  // запоминаем новый максимум
            position = i;       // запоминаем его позицию
        }
    }
    
    // Выводим результат
    cout << "Первый максимальный элемент: " << firstMax << endl;
    cout << "Его позиция: " << position << endl;
    
    return 0;
}